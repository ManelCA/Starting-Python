# Almacenamos la variable del usuario
edadUser = input("Ingresa tu edad: ")
# La mostramos
print("Tu edad es " + edadUser)
# Tipo el dato insertado (es un string)
print(type(edadUser))
# Asignamos el dato pasado por el usuario a int
edadUserNumber = int(edadUser)
# Operamos con esa edad
print(edadUserNumber + 20)
# Conversiones
print(str("20"))
print(float("1.70"))
print(bool("false"))